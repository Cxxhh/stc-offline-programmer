/**
 * @brief 通过USART2发送字符串
 * @param str 要发送的字符串
 */
void USART2_SendString(const char *str)
{
  while (*str)
  {
    // 等待发送寄存器为空
    while (!LL_USART_IsActiveFlag_TXE(USART2))
    {
      // 等待发送就绪
    }
    
    // 发送一个字符
    LL_USART_TransmitData8(USART2, *str);
    str++;
  }
  
  // 等待发送完成
  while (!LL_USART_IsActiveFlag_TC(USART2))
  {
    // 等待传输完成
  }
}

